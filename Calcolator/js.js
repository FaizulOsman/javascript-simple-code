var x = prompt("Enter first number: ");
var y = prompt("Enter last number: ");

var x = parseInt(x, 10)
var y = parseInt(y, 10)

var add = x + y;
document.write(x+ "+" +y+ "=" + add+ "<br/>");

var sub = x - y;
document.write(x+ "-" +y+ "=" + sub+ "<br/>");

var mul = x * y;
document.write(x+ "*" +y+ "=" + mul+ "<br/>");

var div = x / y;
document.write(x+ "/" +y+ "=" + div+ "<br/>");

var mod = x % y;
document.write(x+ "%" +y+ "=" + mod+ "<br/>");